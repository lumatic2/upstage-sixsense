/** 사이드 메뉴 판정 (DR6 step-1)
 *
 *  문제: 예산 1,000원으로 검색하면 `공기밥 1,000원`·`꼬치 1개 1,000원` 만 남아, 그 가게가
 *  "1,000원으로 한 끼 되는 집"처럼 추천됐다. 한 끼가 아니라 곁들임인데 한 끼 자리를 차지한다.
 *
 *  판정을 시트가 아니라 코드에 둔 이유: 시트에 사람이 표시를 달면 새로 들어오는 행마다
 *  누락되고(현재 348행, 계속 증가), 팀원 제보가 들어올 때마다 다시 손이 간다. 대신 규칙을
 *  코드에 두고 **시트 `비고` 열로 뒤집을 수 있게** 했다 — 규칙이 틀린 행은 사람이 이긴다.
 *
 *  사이드로 판정돼도 메뉴 목록에서 사라지지 않는다. 그 가게를 *단독으로 고를 근거*가
 *  되지 못할 뿐이다(`api/recommend.js`).
 *
 *  ⚠ **규칙을 넓히기 전에 비고 열을 먼저 본다** (2026-07-23 사용자 확정).
 *  규칙은 *어느 가게에나 통하는 것*만 담는다. 한 가게 사정인 것을 규칙으로 올리면 다른 데를
 *  부순다 — `모듬튀김 1인 4,500`(무제스시)은 같은 집에 `모듬튀김 22,000`이 따로 있어서
 *  곁들임이고, `Green Salad 7,000`(그 남자의 파스타)은 곁들임이지만 같은 집 `Steak Salad
 *  17,000`은 한 끼다. "튀김 1인"·"샐러드" 를 규칙에 넣었으면 뒤엣것들까지 끌려갔다.
 *  두 행 모두 시트 `비고`="사이드" 로 뒤집었다. 반대로 **고량주·이과두주**처럼 어느 가게에서든
 *  술인 것은 규칙에 넣는 게 맞다(같은 날 8행이 본메뉴로 새고 있었다).
 */

// 시트 `비고` 수동 지정 — 규칙보다 우선한다.
const NOTE_SIDE = /사이드/;
const NOTE_MAIN = /한끼|한 끼/;

// 세트·정식은 술이나 그램 표기를 달고 있어도 한 끼다. 아래 규칙보다 먼저 본다.
// (실측: `동남아 맥주 세트 A 38,000` 이 '맥주' 로 걸렸다.)
const SET = /세트|정식|Set\b/i;

// 다른 메뉴에 얹는 것 — 그 자체로는 끼니가 아니다.
const ADDON = /추가|변경|곱빼기|곱배기|토핑|사리|리필|소스$|드레싱|^Add\b/i;

// 음료·주류. 종류 이름만으로는 안 잡히는 게 있어 브랜드도 넣는다 —
// `청하`·`테라 330ml` 이 카드 맨 앞에 서 있었다(2026-07-20 실측).
// `티\)$` 로 괄호 끝만 보는 이유: 맨 `티$` 를 잡으면 `스파게티` 가 음료가 된다.
// 그래서 차 종류는 낱말을 통째로 적는다.
const DRINK = /음료|콜라|사이다|펩시|탄산|맥주|소주|막걸리|하이볼|에이드|커피|아메리카노|에스프레소|라떼|모카|주스|워터|스무디|아이스티|밀크티|티\)$/;
// 중식·마라탕집의 술이 통째로 빠져 있었다 — `연태고량주 35도 250 24,000`·`이과두주 125` 가
// 본메뉴로 서 있었다(2026-07-23 실측 6행). 규칙이 잡으려던 대상인데 목록에 중국 술이 없었다.
// `얼그레이(FEAT. 양하대곡) 8,000` 은 이름 앞머리가 차라서 술로 안 걸렸다 — 브랜드가 이름
// 어디에 있든 잡히도록 낱말로 둔다(2026-07-23 실측: 이 행이 8천원 예산 통과 근거였다).
const DRINK_BRAND = /청하|테라|카스|하이트|켈리|처음처럼|참이슬|진로|산토리|아사히|칭따오|청도|기린|하이네켄|에델바이스|매화수|백세주|고량주|이과두주|백주|양하대곡|수정방|우량예|마오타이/;

// 밥 단품.
const RICE = /^공기밥|^공깃밥|^밥\s*추가/;

// 단가 표기 — `마라탕 (100g) 2,000원`·`소고기 100g 3,500원` 은 100g 당 가격이지 1인분 값이 아니다.
// 그램 표기 전부를 잡으면 `후쿠오카 숯불함바그 (320g)`·`버터규카츠 스몰(130g)` 같은
// 본메뉴까지 걸린다. 단가 관습인 100g 만 본다(괄호 유무 무관).
const UNIT_PRICE = /\b100\s*g\b/i;

// 낱개 판매 — `꼬치 1개`, `새우튀김 (1P)`.
const SINGLE = /1개$|\(\s*1\s*P\s*\)|\b1pc\b/i;

// 규칙으로 안 잡히는 곁들임. 이름만으로 끼니가 아닌 것이 분명한 것만 넣는다.
const NAMED_SIDE = /반숙란|아지타마고|계란후라이|크로켓|단무지|피클|김치$|쿠키|츄러스/;

/** 이 메뉴가 곁들임인가 — `{ name, note }` 를 받는다. */
export function isSideMenu(menu) {
  const name = (menu?.name ?? "").trim();
  const note = (menu?.note ?? "").trim();
  if (NOTE_MAIN.test(note)) return false;
  if (NOTE_SIDE.test(note)) return true;
  if (!name) return false;
  if (SET.test(name)) return false;
  return ADDON.test(name) || DRINK.test(name) || DRINK_BRAND.test(name) || RICE.test(name)
    || UNIT_PRICE.test(name) || SINGLE.test(name) || NAMED_SIDE.test(name);
}
